    report_lines.append(f"| {name} | {bar} {score}/10 |")
report_lines.append("")
report_lines.append("---")
report_lines.append("")

# ── 2. Service Health ──────────────────────────────────
report_lines.append("## 2. Service Health")
report_lines.append("")

health_events = [e for e in recent_events if e.get("event") in ("service_healthy", "service_down", "service_recovered")]
total_health_checks = len([e for e in health_events if e.get("event") == "service_healthy"])
total_down_events = len([e for e in health_events if e.get("event") == "service_down"])
total_recovered = len([e for e in health_events if e.get("event") == "service_recovered"])
total_incidents = total_down_events

total_checks = total_health_checks + total_down_events
uptime_pct = round(total_health_checks / max(1, total_checks) * 100, 2) if total_checks > 0 else 100.0

report_lines.append(f"- **Uptime**: {uptime_pct}%")
report_lines.append(f"- **Health Checks**: {total_checks}")
report_lines.append(f"- **Downtime Events**: {total_down_events}")
report_lines.append(f"- **Recoveries**: {total_recovered}")
report_lines.append(f"- **Open Incidents**: {max(0, total_incidents - total_recovered)}")
report_lines.append("")
report_lines.append("---")
report_lines.append("")

# ── 3. FinOps Summary ──────────────────────────────────
report_lines.append("## 3. FinOps Summary")
report_lines.append("")

total_calls = len(finops_calls)
total_cost = sum(c.get("cost", 0) for c in finops_calls)
total_tokens = sum(c.get("total_tokens", 0) for c in finops_calls)
cost_per_call = round(total_cost / max(1, total_calls), 6)

report_lines.append(f"- **Total Calls**: {total_calls}")
report_lines.append(f"- **Total Cost**: \${total_cost:.6f}")
report_lines.append(f"- **Total Tokens**: {total_tokens}")
report_lines.append(f"- **Cost per Call**: \${cost_per_call}")
report_lines.append("")

# Model breakdown
model_stats = defaultdict(lambda: {"calls": 0, "cost": 0.0, "tokens": 0})
for c in finops_calls:
    m = c.get("model", "unknown")
    model_stats[m]["calls"] += 1
    model_stats[m]["cost"] += c.get("cost", 0)
    model_stats[m]["tokens"] += c.get("total_tokens", 0)

if model_stats:
    report_lines.append("| Model | Calls | Cost | Tokens |")
    report_lines.append("|-------|-------|------|--------|")
    for m, stats in sorted(model_stats.items()):
        report_lines.append(f"| {m} | {stats['calls']} | \${stats['cost']:.6f} | {stats['tokens']} |")
report_lines.append("")
report_lines.append("---")
report_lines.append("")

# ── 4. Skill Activity ──────────────────────────────────
report_lines.append("## 4. Skill Activity")
report_lines.append("")

skill_events = [e for e in recent_events if e.get("event") == "skill_execution"]
skill_counts = Counter()
