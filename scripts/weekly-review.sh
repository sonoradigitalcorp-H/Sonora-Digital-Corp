if low_score_initiatives:
    for init_name in low_score_initiatives:
        recommendations.append(f"- **🔴 Kill Review**: Initiative \"{init_name}\" scores below 60. Evaluate whether to continue, kill, or restructure.")

# If no events in 24h
if not last_24h:
    recommendations.append("- **🔴 System Check**: No events recorded in the last 24 hours. Verify the event pipeline and autonomous.sh cron job are functioning.")

# Always include a positive if nothing critical
for x in range(len(recommendations)):
    report_lines.append(recommendations[x])

if not recommendations:
    report_lines.append("- **🟢 All Clear**: No critical issues detected. Continue monitoring.")
    # Add growth recommendations
    report_lines.append("- **💡 Growth Tip**: Review initiative registry for scale candidates (KEEP → SCALE).")
    report_lines.append("- **💡 Knowledge Tip**: Ensure all OS are wired to knowledge capture for continuity.")

report_lines.append("")
report_lines.append("---")
