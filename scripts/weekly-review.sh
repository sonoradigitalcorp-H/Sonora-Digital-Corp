            if line:
                try:
                    ev = json.loads(line)
                    events.append(ev)
                except json.JSONDecodeError:
                    pass

# Filter last 7 days
recent_events = []
for ev in events:
    ts = ev.get("timestamp", "")
    try:
        ev_dt = datetime.fromisoformat(ts.replace('Z', '+00:00'))
        if ev_dt >= week_ago:
            recent_events.append(ev)
