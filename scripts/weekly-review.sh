with open("${REPORT_FILE}", "w") as f:
    f.write("\n".join(report_lines) + "\n")

print(f"Report written: ${REPORT_FILE}")
print(f"Enterprise Score: {current_total}/100{delta_str}")
print(f"Events analyzed: {len(recent_events)} (7d), {len(last_24h)} (24h)")
print(f"FinOps calls: {total_calls}")
print(f"Skills executed: {len(skill_events)}")
print(f"Recommendations: {len(recommendations)}")
