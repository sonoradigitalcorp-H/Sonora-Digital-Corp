if uptime_pct < 99.0:
    recommendations.append("- **🔴 Infra Review**: Uptime is {:.2f}% (below 99%). Review service reliability and auto-recovery procedures.".format(uptime_pct))

# If cost per call > $0.01
if cost_per_call > 0.01:
    recommendations.append("- **🟡 Model Optimization**: Cost per call is \${:.6f} (above \$0.01). Consider switching to cheaper models or optimizing prompt lengths.".format(cost_per_call))

