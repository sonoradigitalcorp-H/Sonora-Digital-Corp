    p = se.get("payload", {})
    if isinstance(p, dict):
        skill_name = p.get("skill", se.get("producer", "unknown"))
    else:
        skill_name = se.get("producer", "unknown")
    status = p.get("status", "unknown") if isinstance(p, dict) else "unknown"
    skill_counts[skill_name] += 1
    if status == "pass":
        skill_pass[skill_name] += 1
    elif status == "fail":
        skill_fail[skill_name] += 1

if skill_counts:
    report_lines.append("| Skill | Runs | Pass | Fail | Pass Rate |")
    report_lines.append("|-------|------|------|------|-----------|")
    for skill_name in sorted(skill_counts):
        total_runs = skill_counts[skill_name]
        passed = skill_pass[skill_name]
        failed = skill_fail[skill_name]
        rate = round(passed / max(1, total_runs) * 100, 1)
        report_lines.append(f"| {skill_name} | {total_runs} | {passed} | {failed} | {rate}% |")
else:
    report_lines.append("No skill executions recorded in the last 7 days.")
report_lines.append("")
report_lines.append("---")
