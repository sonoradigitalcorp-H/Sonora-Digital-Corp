initiatives = []
if os.path.isdir(initiative_dir):
    for fname in sorted(os.listdir(initiative_dir)):
        if fname.endswith(".md") and fname not in ("REGISTRY.md", "initiative-TEMPLATE.md"):
            fpath = os.path.join(initiative_dir, fname)
            with open(fpath) as f:
                content = f.read()
            name_match = re.search(r'# Initiative:\s*(.+?)(?:\n|$)', content)
            initiative_name = name_match.group(1).strip() if name_match else fname.replace(".md", "")
            score_match = re.search(r'\*\*Total Score:\s*(\d+)/100', content)
            initiative_score = int(score_match.group(1)) if score_match else 0
            deadline_match = re.search(r'Deadline:\s*(\d{4}-\d{2}-\d{2})', content)
            deadline_str = deadline_match.group(1) if deadline_match else ""
            days_remaining = ""
            if deadline_str:
                try:
                    deadline_dt = datetime.strptime(deadline_str, "%Y-%m-%d").replace(tzinfo=timezone.utc)
                    remaining = (deadline_dt - now).days
                    if remaining > 0:
                        days_remaining = f"{remaining} days"
                    elif remaining == 0:
                        days_remaining = "Due today"
                    else:
                        days_remaining = f"Overdue by {-remaining} days"
                except ValueError:
                    days_remaining = "Invalid date"
            initiatives.append((initiative_name, initiative_score, days_remaining))

if initiatives:
    report_lines.append("| Initiative | Score | Deadline | Days Remaining |")
    report_lines.append("|------------|-------|----------|----------------|")
    for name, score, days_left in initiatives:
        status_flag = "⚠️ " if score < 60 else "✅ "
        report_lines.append(f"| {status_flag}{name} | {score}/100 | | {days_left} |")
else:
    report_lines.append("No active initiatives found.")
report_lines.append("")
report_lines.append("---")
report_lines.append("")

