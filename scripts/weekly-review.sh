    total_event_count = sum(event_type_counts.values())
    report_lines.append(f"**Total events (7d)**: {total_event_count}")
    report_lines.append("")
    report_lines.append("| Event Type | Count | % of Total |")
    report_lines.append("|------------|-------|------------|")
    for etype, count in event_type_counts.most_common(10):
        pct = round(count / total_event_count * 100, 1)
        report_lines.append(f"| {etype} | {count} | {pct}% |")
else:
    report_lines.append("No events recorded in the last 7 days.")
