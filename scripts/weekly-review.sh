def compute_score(evts, fcalls):
    revenue_evts = [e for e in evts if e.get("event") == "revenue_recorded"]
    revenue_score = min(10, len(revenue_evts)) if revenue_evts else 1

    scale_evts = [e for e in evts if e.get("event") in ("scaled_up", "scaled_down")]
    scalability_score = min(10, len(scale_evts) + 3)

    skill_evts = [e for e in evts if e.get("event") == "skill_execution"]
    os_used = set()
    for s in skill_evts:
        p = s.get("payload", {})
        if isinstance(p, dict):
            os_used.add(p.get("parent_os", ""))
    reusability_score = min(10, len(os_used))

    auto_evts = len(skill_evts)
    total_ops = len(evts)
    automation_score = min(10, round(auto_evts / max(1, total_ops) * 10)) if total_ops > 0 else 1

    knowledge_evts = [e for e in evts if e.get("event") == "knowledge_stored"]
    knowledge_score = min(10, len(knowledge_evts))

    healthy = len([e for e in evts if e.get("event") == "service_healthy"])
    down = len([e for e in evts if e.get("event") == "service_down"])
    total_health = healthy + down
    reliability_score = round(healthy / max(1, total_health) * 10) if total_health > 0 else 5

    recovered = len([e for e in evts if e.get("event") == "service_recovered"])
    total_incidents = down + recovered
    founder_score = min(10, round(recovered / max(1, total_incidents) * 10)) if total_incidents > 0 else 1

    event_types = len(set(e.get("event", "") for e in evts))
    simplicity_score = max(0, 10 - event_types // 10) if event_types > 0 else 10

    satisfaction_evts = [e for e in evts if e.get("event") == "satisfaction_recorded"]
    customer_score = min(10, len(satisfaction_evts)) if satisfaction_evts else 1

    total_cost = sum(c.get("cost", 0) for c in fcalls)
    total_calls = len(fcalls)
    cost_per_call_val = total_cost / max(1, total_calls)
    finops_score = max(0, 10 - round(cost_per_call_val / 0.001)) if total_calls > 0 else 1

    metrics = {
        "Revenue Impact": revenue_score,
        "Scalability": scalability_score,
        "Reusability": reusability_score,
        "Automation Impact": automation_score,
        "Knowledge Impact": knowledge_score,
        "Reliability": reliability_score,
        "Founder Independence": founder_score,
        "Operational Simplicity": simplicity_score,
        "Customer Value": customer_score,
        "FinOps Efficiency": finops_score,
    }
    total = sum(metrics.values())
    return total, metrics

current_total, current_metrics = compute_score(recent_events, finops_calls)

# Compare with last week's report if exists
last_week_total = None
last_week_file = None
try:
    five_days_ago = (datetime.now(timezone.utc) - timedelta(days=5)).strftime('%Y-%m-%d')
    all_reports = sorted(os.listdir("${REPORTS_DIR}")) if os.path.isdir("${REPORTS_DIR}") else []
    for rf in reversed(all_reports):
        if rf.startswith("weekly-") and rf != f"weekly-{report_date}.md":
            last_week_file = rf
            break
    if last_week_file:
        with open(os.path.join("${REPORTS_DIR}", last_week_file)) as f:
            for line in f:
                m = re.search(r'Enterprise Score:\s*(\d+)/100', line)
                if m:
                    last_week_total = int(m.group(1))
                    break
except (OSError, ValueError):
    pass

delta_str = ""
if last_week_total is not None:
    diff = current_total - last_week_total
    if diff > 0:
        delta_str = f" (+{diff} from last week)"
    elif diff < 0:
        delta_str = f" ({diff} from last week)"
    else:
        delta_str = " (no change from last week)"

report_lines.append(f"**Enterprise Score: {current_total}/100**{delta_str}")
status_emoji = "🟢 PASS" if current_total >= 60 else "🔴 FAIL"
report_lines.append(f"**Status**: {status_emoji} (threshold: ≥ 60)")
report_lines.append("")
report_lines.append("| Metric | Score |")
report_lines.append("|--------|-------|")
for name, score in current_metrics.items():
